/// 말트임 30일 커리큘럼(98문장)에 등장하는 어휘의 한자 표기를 히라가나
/// 읽기로 변환하기 위한 사전.
///
/// STT는 정확한 발음을 표준 한자 표기로 인식하지만, 커리큘럼의 목표 문장은
/// 모두 히라가나로 작성되어 있다. 글자 단위 채점 전에 이 사전으로 STT 결과의
/// 한자를 히라가나로 치환하면, 한자/가나 표기 차이로 인한 오채점을 줄일 수 있다.
///
/// - 활용형 동사(食べます 등)는 어간 한자 한 글자만 읽기로 치환하고,
///   送り仮名(きます/ます 등)는 STT 결과에서도 이미 히라가나이므로 그대로 둔다.
/// - 복합어(学校, 勉強 등)는 단어 전체를 치환한다.
/// - 키는 길이 내림차순으로 정렬되어 있어, 복합어가 한 글자 항목보다
///   먼저 매칭된다.
class KanjiReadingMap {
  KanjiReadingMap._();

  static const Map<String, String> _map = {
    // 복합어 (2글자 이상)
    '頑張': 'がんば',
    '一緒': 'いっしょ',
    '日本語': 'にほんご',
    '日本': 'にほん',
    '学校': 'がっこう',
    '仕事': 'しごと',
    '友達': 'ともだち',
    '勉強': 'べんきょう',
    '大変': 'たいへん',
    '毎日': 'まいにち',
    '昨日': 'きのう',
    '寿司': 'すし',
    // 한 글자(어간) 동사·명사
    '行': 'い',
    '来': 'き',
    '帰': 'かえ',
    '食': 'た',
    '飲': 'の',
    '買': 'か',
    '見': 'み',
    '待': 'ま',
    '聞': 'き',
    '書': 'か',
    '教': 'おし',
    '入': 'はい',
    '思': 'おも',
    '話': 'はな',
    '分': 'わ',
    '遊': 'あそ',
    '始': 'はじ',
    '休': 'やす',
    '寝': 'ね',
    '疲': 'つか',
    '洗': 'あら',
    '高': 'たか',
    '難': 'むずか',
    '楽': 'たの',
    '水': 'みず',
    '家': 'うち',
    '店': 'みせ',
    '手': 'て',
    '前': 'まえ',
    '後': 'あと',
  };

  /// 길이 내림차순으로 정렬된 키 목록 (긴 항목을 먼저 매칭하기 위함).
  static final List<String> _orderedKeys = _map.keys.toList()
    ..sort((a, b) => b.length.compareTo(a.length));

  /// [input]에서 사전에 등록된 한자(구간)를 히라가나 읽기로 치환한다.
  /// 등록되지 않은 한자는 그대로 둔다(기존 cross-script 처리로 폴백).
  static String convert(String input) {
    var result = input;
    for (final key in _orderedKeys) {
      if (result.contains(key)) {
        result = result.replaceAll(key, _map[key]!);
      }
    }
    return result;
  }
}
